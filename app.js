// chain different methods to one common route string
app
  .route('/api/v1/tours')
  .get(getAllTours).post(createTour);

app
  .route('/api/v1/tours/:id')
  .get(getTour)
  .patch(updateTour)
  .delete(deleteTour);

const port = 3001;

app.listen(port, () => {
  console.log(`App running on port ${port}...`);
});
